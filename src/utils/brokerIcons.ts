// ── Domain → icon mapping ────────────────────────────────────────────────────

/** Ordered list of domain substrings to match against broker URLs. */
const DOMAIN_MAP: Array<{ pattern: string; icon: BrokerIcon }> = [
  { pattern: "hivemq.com", icon: HIVEMQ },
  { pattern: "mosquitto.org", icon: MOSQUITTO },
  { pattern: "emqx.io", icon: MQTT_GENERIC },
];

/**
 * Resolve a broker icon from its WebSocket URL.
 * Matches known domain substrings; returns the generic MQTT icon for unknown brokers.
 */
export function getBrokerIcon(brokerUrl: string): BrokerIcon {
  const lower = brokerUrl.toLowerCase();
  for (const entry of DOMAIN_MAP) {
    if (lower.includes(entry.pattern)) {
      return entry.icon;
    }
  }
  return MQTT_GENERIC;
}
